            <h1>Ziekmelden om extreme hooikoorts, patiënten snakken naar regen</h1>
            <p>Iedereen kent wel iemand met hooikoorts. Waar de één de hele buurt bij
                elkaar niest, snottert de ander de hele dag door. Sommigen hebben er zoveel
                last van dat werken of studeren niet meer gaat.

                Een paar weken geleden waarschuwden experts vanwege de
                combinatie van het natte en warme voorjaar en de droogte van de laatste weken voor
                een pollenexplosie. Dat heeft basisschoolleraar Lisette ter Beest geweten.
                "Het is deze weken echt heel heftig. Normaal
                heb ik al medicatie van de huisarts, maar nu heb ik zelfs een puffer meegekregen
                tegen de klachten."

                Het werd deze week zo erg dat Ter Beest voor het eerst halverwege
                de dag haar werk moest staken. "Door al dat hoesten was ik mijn stem
                kwijtgeraakt en moest ik naar huis. Dat heb ik nog nooit gehad. Het zit bij mij echt in
                de luchtwegen, dus ik moet veel niezen, veel hoesten, en heb jeuk aan mijn ogen,
                mijn keel en gehemelte."</p>
            <img src="Img/pollen.jpg" alt="" id="img3">
            <p>Met milde klachten kun je prima functioneren met medicijnen van de drogist of een extra middeltje van de huisarts. Maar
                sommigen hebben er zoveel last van dat ze naar een specialist moeten in
                het ziekenhuis. "Het heet niet voor niets hooikoorts: als je er echt ziek
                van wordt, kan het voelen alsof je de griep hebt", zegt kinderarts en
                allergoloog in het Deventer Ziekenhuis Monique Gorissen in het radioprogramma
                Nieuws en Co.

                Hoewel hooikoorts zich normaliter pas op latere leeftijd ontwikkelt, ziet de arts dat de
                klachten zich op steeds jongere leeftijd voordoen. Ook wordt de groep met extreme
                klachten volgens haar steeds groter.</p>
            <br>
            <br>
            <p>Volgens allergoloog Gorissen zijn we voorlopig nog niet af van die heftige hooikoortsseizoenen. "We weten het natuurlijk nooit precies, maar de verwachting is dat hier in de zomer minder neerslag valt en dat het warmer is. Die twee dingen
                samen maken dat het flink stuift", aldus de arts. Slecht nieuws voor
                hooikoortspatiënten dus.

                Maar voor de korte termijn is er ook goed nieuws: de regendans van Vincent
                heeft geholpen. Vanaf morgen valt er her en der een lokale bui en ook de
                komende dagen zal er af en toe wat regen vallen. Die regen zorgt voor schone
                lucht en vermindert het aantal pollen: een beetje verlichting dus voor al die
                hooikoortspatiënten in Nederland.

                Ter Beest kan in ieder geval niet wachten tot de eerste druppels vallen: "Het is zo'n
                cliché, want als het heet is, klagen we over de hitte. Is het koud, dan klagen we dat
                het warmer moet zijn. Maar een klein beetje regen zou nu toch wel lekker zijn. Niet
                alleen voor mij, maar ook voor de natuur."</p>
