            <h1>Resten in graf Leipzig inderdaad van Rijksdagbrandstichter Van der Lubbe</h1>
            <p>De stoffelijke resten in een graf in Leipzig zijn inderdaad van Rijksdagbrandstichter Marinus van der Lubbe, concluderen forensisch pathologen na maanden onderzoek. Tot nu toe was daar nog onzekerheid over. Om duidelijkheid te krijgen, werden de overblijfselen begin dit jaar opgegraven. Onderzoekers vergeleken dna-monsters van
                een tand met een speekselmonster van een kleinzoon van de broer van Van der Lubbe.

                Van der Lubbe was een 24-jarige partijloze communist uit Leiden.
                Op de avond van 27 februari 1933 stichtte hij brand in het
                Rijksdaggebouw in Berlijn, dat na enkele minuten in lichterlaaie stond
                en binnen twee uur volledig uitbrandde. Over de toedracht van de brand
                zijn verschillende lezingen: onduidelijk is of Van der Lubbe alleen heeft
                gehandeld of hulp kreeg van andere communisten.

                Ook zijn er scenario's waarin de nazi's verantwoordelijk worden gesteld voor
                de brand, die ze aangrepen om in brede kring af te rekenen met communisten.
                Van der Lubbe beweerde bij zijn arrestatie dat hij de brand had gesticht om
                de Duitse arbeiders te waarschuwen voor het gevaar van het nationaalsocialisme.

                In het najaar van 1933 moest Van der Lubbe zich verantwoorden bij het
                Rijksgerechtshof in Leipzig. Daarbij maakte hij een apathische indruk.
                Destijds werd verondersteld dat hij scopolamine toegediend had gekregen,
                ook wel "waarheidsserum" of "duivelsadem" genoemd. Bij hoge doses veroorzaakt
                dat een delier, waarna vermoeidheid, slaperigheid en geheugenverlies optreden.

                Daarom zochten toxicologen afgelopen maanden ook naar bewijs voor het verhaal
                dat Van der Lubbe gedrogeerd was. Dat vonden zij niet. Toch is het volgens
                hen nog steeds mogelijk dat Van der Lubbe onder invloed was van drugs tijdens het
                proces. "89 jaar na het overlijden is het erg moeilijk om dergelijk wetenschappelijk
                bewijs te leveren." Van der Lubbe werd ter dood veroordeeld en kort na het vonnis
                onthoofd.</p>

            <h2>Historische mythe ontkracht</h2>
            <p>De onderzoekers zijn er ook in geslaagd om een
                historische mythe te ontkrachten. Van der Lubbe zou
                extra diep begraven liggen om te voorkomen dat hij opnieuw wordt opgegraven.
                Maar dat blijkt niet te kloppen: de resten van de jonge communist werden gevonden
                op een normale diepte van twee meter.

                Van der Lubbe werd in januari opgegraven op initiatief van de Paul Benndorf-Gesellschaft, een stichting die zich bezighoudt met het onderhoud van historische graven. De stichting wilde weten
                waar Van der Lubbe precies begraven was, om daar een monument te plaatsen.</p>
